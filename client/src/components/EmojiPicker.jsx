export default function EmojiPicker({ onSelect, onClose }) {
  const [search, setSearch] = useState('')
  const [category, setCategory] = useState(Object.keys(EMOJIS)[0])

  const allEmojis = search
    ? Object.values(EMOJIS).flat().filter(e => e.toLowerCase().includes(search.toLowerCase()))
    : EMOJIS[category] || []

  return (
    <div className="picker-panel emoji-picker" onClick={(e) => e.stopPropagation()}>
      <div className="picker-search">
        <input
          type="text"
          placeholder="Search emoji..."
          value={search}
          onChange={(e) => setSearch(e.target.value)}
          autoFocus
        />
      </div>
      {!search && (
        <div className="emoji-categories">
          {Object.keys(EMOJIS).map(cat => (
            <button
              key={cat}
              className={`emoji-cat-btn ${category === cat ? 'active' : ''}`}
              onClick={() => setCategory(cat)}
            >
              {EMOJIS[cat][0]}
            </button>
          ))}
        </div>
      )}
      <div className="emoji-grid">
        {allEmojis.map((emoji, i) => (
          <button
            key={`${emoji}-${i}`}
            className="emoji-item"
            onClick={() => { onSelect(emoji); onClose() }}
          >
            {emoji}
          </button>
        ))}
      </div>
    </div>
  )
}
